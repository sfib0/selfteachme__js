/*
  # Expressions
  - An expression is a combination of values, variables, and operators which
  computes a value.
  - Anything which will be evaluated to a value is an expression.
  - The computation is called an evaluation.
*/

// For example:
a = 1; 
b = 2;
a + b; // expression
4 / 2; // expression
"Hello" + "World"; // expression

